#pragma once
#include "gbytearray.h"
#include "bastypes.h"
#include <vector>

using namespace std;

//================================================================================
/// 模型TID文件处理类
/// 处理TID（Tile ID）数据的读写操作
//================================================================================
class Ci_ModelTidFile
{
public:
	//================================================================================
	/// 从字节数组读取TID数据
	///
	/// @param [in]  in   输入字节数组
	/// @param [out] out  输出TID数据向量（二维向量）
	/// @return 操作结果
	///         - gisLONG > 0：读取成功
	///         - gisLONG <= 0：读取失败
	//================================================================================
	gisLONG From(const CGByteArray& in, vector<vector<gisINT64>>& out);

	//================================================================================
	/// 将TID数据写入字节数组
	///
	/// @param [in]  in   输入TID数据向量（二维向量）
	/// @param [out] out  输出字节数组
	/// @return 操作结果
	///         - gisLONG > 0：写入成功
	///         - gisLONG <= 0：写入失败
	//================================================================================
	gisLONG To(const vector<vector<gisINT64>>& in, CGByteArray& out);
};